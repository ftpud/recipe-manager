    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int[] indexes = tblProducts.getSelectedRows();
        for(int index : indexes)
        {
            int productID = (int)productTableModel.getValueAt(tblProducts.convertRowIndexToModel(index), 0);
            System.out.println(productID);            
            RecipeProvider.DeleteProduct(productID);
        }
        UpdateProductModel();
    }//GEN-LAST:event_btnDeleteActionPerformed

    
    
    private void UpdateProductModel()
    {
        productTableModel.setRowCount(0);
        for(ProductModel product : RecipeProvider.GetProductList())
        {
            Object[] row = {product.getId(), product.getName(), product.getComment()};
            productTableModel.addRow(row);
        }
        
    }
    
    
    
    
